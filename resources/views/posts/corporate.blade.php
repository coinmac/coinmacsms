    <p>I heard of people missing important job interviews across Nigeria due to DND fully activated on their network. The good news is: you can unblock/deactivate Do-Not-Disturb on your SIM card.</p>
    
    
    
    DND service features the following:
    
    <p>Fully Blocked mode:
    allows you to opt-out totally from receiving promotional messages via SMS and IVR.
    
    <p>Partially Blocked mode:
    allows you to filter service categories for promotional messages you wish to receive. Any category you do not select is blocked.
    Steps To Activate/Deactivate Do-Not-Disturb (DND) On MTN & Airtel Nigeria
    You will be able to perform the following DND activities via SMS on your cell phone. They include: View Status, Activate full blocking, Activate partial blocking, Opt out of DND.</p>
    
    <p>#1. If you want to STOP receiving all promotional messages, Bulk SMS, etc on your phone Text STOP to 2442. This is will ACTIVATE DND on your SIM. Goodbye to those annoying messages.</p>
    
    <p>#2. To receive ALL messages on your SIM card (both political, promotional, Bulk SMS, etc), Text ALLOW to 2442. This will DEACTIVATE DND on your SIM. Your days of receiving annoying messages have just begun.</p>
    
    <p>#3. To check DND status whether it is activated or not on your SIM, Text STATUS to 2442.</p>
