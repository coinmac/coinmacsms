    What is Do Not Disturb Service?
    
    <p>Not hard as you think – it’s simply a service which allows ALL mobile network users in Nigeria to opt out or quit getting SMS Spam messages / promotional messages from third-party services (Bulk SMS, VAS, Promos, and so forth). Believe me, the days of receiving customized political messages on your phone is over, oh yes!</p>
    
